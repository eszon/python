# Disseminating Variable

var01 = str(input('Digite algo: '))

print('The primitive type of this variable value is: {}'.format(type(var01)))
print('Only spaces: {} '.format(var01.isspace()))
print('It is a number: {}'.format(var01.isnumeric()))
print('It is a alphabetic: {}'.format(var01.isalpha()))
print('It is lowercase: {} '.format(var01.islower()))
print('It is uppercase: {}'.format(var01.isupper()))
print('It is a Captalize: {}'.format(var01.istitle()))






